arr = [2,3,4];
console.log(arr);  // 2 3 4

arr.push(5);
console.log(arr);  // 2 3 4 5

arr.pop();
console.log(arr);  // 2 3 4

arr.unshift(1);
console.log(arr);  // 1 2 3 4

arr.shift(1);
console.log(arr);  // 2 3 4


// We can store elements of different data type in arrays
brr = [1,"trigun",4.5];
console.log(brr);  // 1 'trigun' 4.5
